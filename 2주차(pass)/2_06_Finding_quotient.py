def solution(num1, num2):
    return num1//num2

#파이썬에서의 나누기
# "/" : 단순 나눗셈 - 가장 기본적인 나눗셈 (float형)
# "//" : 나눗셈의 몫 - 나눗셈 결과의 '몫'을 가져옴
# "%" : 나눗셈의 나머지 - 나눗셈 결과의 '나머지'를 가져옴
# divmod(): 나눗셈의 몫과 나머지(튜플 형식) - 나눗셈 결과의 '몫'과 '나머지'를 한번에 가져옴 # 나눗셈 연산을 수행하고, 그 연산의 결과인 몫과 나머지를 튜플형식으로 한번에 가져옵니다. # 따라서 이 결과를 변수에 넣어 사용할 때에는, 2개의 변수가 필요합니다.